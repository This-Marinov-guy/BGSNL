const fs = require("node:fs");
const path = require("node:path");

const REGION_LABELS = {
  amsterdam: "Amsterdam",
  breda_tilburg: "Breda and Tilburg",
  eindhoven: "Eindhoven",
  groningen: "Groningen",
  leiden_hague: "Leiden and The Hague",
  leeuwarden: "Leeuwarden",
  maastricht: "Maastricht",
  rotterdam: "Rotterdam",
  netherlands: "Netherlands",
};

const EXCLUDED_ARCHIVE_EVENT_IDS = new Set([
  "rotterdam-2025-04-05-movie-night",
]);

function buildPastEventsArchiveFromTickets({
  inputPath,
  now = new Date(),
  limit = 120,
} = {}) {
  if (!inputPath) throw new Error("Missing inputPath");
  const source = fs.readFileSync(inputPath, "utf8");
  const delimiter = detectDelimiter(inputPath, source);
  const rows = parseDelimited(source, delimiter);
  if (rows.length < 2) return [];

  const headers = rows[0].map((header) => cleanText(header));
  const grouped = new Map();
  const today = startOfUtcDay(now);

  for (const values of rows.slice(1)) {
    const row = rowObject(headers, values);
    const rawTitle = cleanText(firstValue(row, ["Name Event 2", "Name Event", "Name Event 1"]));
    const title = cleanArchiveEventTitle(rawTitle);
    const explicitDate = toIsoDate(firstValue(row, ["Date", "Event Date"]));
    const purchaseDate = toIsoDate(firstValue(row, ["Timestamp"]));
    const region = normalizeRegion(firstValue(row, ["Region"]));
    const location = cleanText(firstValue(row, ["Location"]));

    if (!title || !region) continue;

    const rawSlug = slugify(rawTitle || title);
    const titleSlug = slugify(title);
    const key = explicitDate
      ? [region, "dated", explicitDate, titleSlug, slugify(location)].join("|")
      : [region, "archive", rawSlug].join("|");
    const group = grouped.get(key) || {
      rawSlug,
      titleSlug,
      title,
      region,
      regionName: REGION_LABELS[region] || titleCase(region.replace(/_/g, " ")),
      explicitDate,
      purchaseDates: [],
      location: "",
      registrationCount: 0,
      publicSignals: emptyPublicSignals(),
    };

    group.registrationCount += 1;
    if (location && !group.location) group.location = location;
    if (purchaseDate) group.purchaseDates.push(purchaseDate);
    collectPublicSignals(group.publicSignals, {
      preferences: firstValue(row, ["Preferences"]),
      ticket: firstValue(row, ["Ticket"]),
    });
    grouped.set(key, group);
  }

  return [...grouped.values()]
    .map((group) => eventFromGroup(group, today))
    .filter(Boolean)
    .sort(sortArchiveEvents)
    .slice(0, limit);
}

function eventFromGroup(group, today) {
  const recoveredDate = group.explicitDate || latestDate(group.purchaseDates);
  if (recoveredDate && startOfUtcDay(new Date(`${recoveredDate}T00:00:00.000Z`)) >= today) {
    return null;
  }

  const recoveredFromPurchase = !group.explicitDate && Boolean(recoveredDate);
  const idSlug = recoveredFromPurchase ? group.rawSlug : group.titleSlug;
  const id = recoveredDate
    ? `${group.region}-${recoveredDate}-${idSlug}`
    : `${group.region}-archive-${group.rawSlug}`;
  if (EXCLUDED_ARCHIVE_EVENT_IDS.has(id)) return null;

  const tagText = [
    group.title,
    group.location,
    publicSignalText(group.publicSignals),
  ].join(" ");
  const event = {
    id,
    title: group.title,
    region: group.region,
    regionName: group.regionName,
    date: recoveredDate,
    year: recoveredDate ? Number(recoveredDate.slice(0, 4)) : null,
    location: group.location,
    registrationCount: group.registrationCount,
    url: "",
    tags: inferTags(tagText),
  };

  if (recoveredFromPurchase) {
    event.dateSource = "last-ticket-purchase";
    event.sourceStatus = "archive-recovered";
  } else if (!recoveredDate) {
    event.sourceStatus = "archive-only";
  }

  event.summary = buildEventSummary(event, group.publicSignals);
  return event;
}

function writePastEventsArchive({
  archive,
  jsonOutPath,
  jsOutPath,
  generatedAt = new Date().toISOString(),
} = {}) {
  if (!Array.isArray(archive)) throw new Error("archive must be an array");
  if (!jsonOutPath) throw new Error("Missing jsonOutPath");
  if (!jsOutPath) throw new Error("Missing jsOutPath");

  const payload = {
    generatedAt,
    privacy:
      "Generated from event-level export fields and aggregated registration-choice signals only. Personal contact, attendee identity, ticket identifier, ticket file URL, and payment fields are intentionally excluded.",
    stats: buildStats(archive),
    events: archive,
  };

  fs.mkdirSync(path.dirname(jsonOutPath), { recursive: true });
  fs.writeFileSync(jsonOutPath, `${JSON.stringify(payload, null, 2)}\n`, "utf8");

  const jsSource = [
    "// Generated by scripts/export-past-events-from-tickets.js.",
    "// Public event summaries only; attendee and payment data are intentionally excluded.",
    "import archive from \"./past-events-archive.json\";",
    "",
    "export const PAST_EVENTS_ARCHIVE_GENERATED_AT = archive.generatedAt;",
    "export const PAST_EVENTS_ARCHIVE_STATS = archive.stats;",
    "export const PAST_EVENTS_ARCHIVE = archive.events;",
    "",
  ].join("\n");
  fs.writeFileSync(jsOutPath, jsSource, "utf8");

  return payload;
}

function buildStats(archive) {
  const regions = new Set();
  const years = new Set();
  const datedEvents = archive.filter((event) => event.date);
  for (const event of archive) {
    if (event.region) regions.add(event.region);
    if (event.year) years.add(event.year);
  }

  return {
    totalEvents: archive.length,
    totalRegions: regions.size,
    totalRegistrations: archive.reduce((sum, event) => sum + (event.registrationCount || 0), 0),
    years: [...years].sort((a, b) => b - a),
    latestDate: datedEvents[0]?.date || "",
    oldestDate: datedEvents.at(-1)?.date || "",
  };
}

function emptyPublicSignals() {
  return {
    menuChoices: new Map(),
    wineChoices: new Map(),
    teamSignups: false,
    singlePlayerSignups: false,
    quiz: false,
    workshop: false,
    borrel: false,
  };
}

function collectPublicSignals(signals, { preferences, ticket }) {
  const preferenceText = cleanText(preferences);
  const ticketText = cleanText(ticket);

  if (isPublicPreference(preferenceText)) {
    const lower = preferenceText.toLowerCase();
    if (/menu|меню/.test(lower)) {
      for (const choice of extractPreferenceAnswers(preferenceText, /menu|меню/i)) {
        addSignalChoice(signals.menuChoices, choice);
      }
    }
    if (/wine|вино/.test(lower)) {
      for (const choice of extractPreferenceAnswers(preferenceText, /wine|вино/i)) {
        addSignalChoice(signals.wineChoices, choice);
      }
    }
    if (/team/i.test(preferenceText)) signals.teamSignups = true;
    if (/quiz/i.test(preferenceText)) signals.quiz = true;
    if (/workshop|public speaking|confidence|negotiate/i.test(preferenceText)) signals.workshop = true;
    if (/borrel/i.test(preferenceText)) signals.borrel = true;
  }

  if (isPublicTicketLabel(ticketText)) {
    if (/teams?/i.test(ticketText)) signals.teamSignups = true;
    if (/single[-\s]?player/i.test(ticketText)) signals.singlePlayerSignups = true;
  }
}

function isPublicPreference(value) {
  const text = cleanText(value);
  if (!text || /^(none|-|n\/a|na)$/i.test(text)) return false;
  if (looksSensitive(text)) return false;
  return /menu|меню|wine|вино|team|quiz|workshop|public speaking|confidence|negotiate|borrel/i.test(text);
}

function isPublicTicketLabel(value) {
  const text = cleanText(value);
  if (!text || /^(regular|standard|member|vip|guest|n\/a|na|-|none)$/i.test(text)) return false;
  if (/https?:\/\//i.test(text) || /\.(webp|png|jpe?g|pdf)(\?|$)/i.test(text)) return false;
  if (looksSensitive(text)) return false;
  return /teams?|single[-\s]?player/i.test(text);
}

function looksSensitive(value) {
  const text = cleanText(value);
  return /@|\+\d{6,}|https?:\/\/|bgsg-(guest|member)-tickets|ticket limit|price/i.test(text);
}

function extractPreferenceAnswers(text, keywordRe) {
  const cleaned = cleanText(text);
  const answers = [];
  const questionChunks = cleaned.split(/(?=\b(?:What|Would|Are|Do|Which|Какво|От|За|Желаете|В коя)\b)/);

  for (const chunk of questionChunks) {
    const separatorIndex = chunk.indexOf(":");
    if (separatorIndex < 0) continue;
    const question = chunk.slice(0, separatorIndex);
    if (!keywordRe.test(question)) continue;
    const answer = chunk.slice(separatorIndex + 1);
    if (answer) answers.push(cleanPublicChoice(answer));
  }

  if (answers.length === 0) {
    const separatorIndex = cleaned.indexOf(":");
    const question = separatorIndex >= 0 ? cleaned.slice(0, separatorIndex) : cleaned;
    const answer = separatorIndex >= 0 ? cleaned.slice(separatorIndex + 1) : "";
    if (!keywordRe.test(question)) return answers;
    if (answer) answers.push(cleanPublicChoice(answer));
  }

  return answers.filter(Boolean);
}

function cleanPublicChoice(value) {
  return cleanText(value)
    .replace(/\b\d+(?:[.,]\d+)?\s*(?:euro|eur|€)\b/gi, "")
    .replace(/\s+/g, " ")
    .replace(/^[,;:.\s]+|[,;:.\s]+$/g, "")
    .slice(0, 80);
}

function addSignalChoice(map, choice) {
  const cleaned = cleanPublicChoice(choice);
  if (!cleaned || looksSensitive(cleaned)) return;
  map.set(cleaned, (map.get(cleaned) || 0) + 1);
}

function publicSignalText(signals) {
  return [
    ...signals.menuChoices.keys(),
    ...signals.wineChoices.keys(),
    signals.teamSignups ? "team signups games" : "",
    signals.singlePlayerSignups ? "single player games" : "",
    signals.quiz ? "quiz" : "",
    signals.workshop ? "workshop" : "",
    signals.borrel ? "borrel" : "",
  ].join(" ");
}

function latestDate(values) {
  return values.filter(Boolean).sort().at(-1) || "";
}

function topChoices(map, limit = 3) {
  return [...map.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .map(([choice]) => choice)
    .slice(0, limit);
}

function buildEventSummary(event, signals) {
  const core = baseEventSummary(event);
  const details = [];
  const menuChoices = topChoices(signals.menuChoices);
  const wineChoices = topChoices(signals.wineChoices);

  if (menuChoices.length > 0) {
    details.push(`Registration included menu choices such as ${formatList(menuChoices)}.`);
  }
  if (wineChoices.length > 0) {
    details.push(`Wine preferences such as ${formatList(wineChoices)} were part of the sign-up.`);
  }
  if (signals.teamSignups && signals.singlePlayerSignups) {
    details.push("People could join through team and single-player sign-ups.");
  } else if (signals.teamSignups) {
    details.push("Team sign-ups helped people join the activity together.");
  } else if (signals.singlePlayerSignups) {
    details.push("Single-player sign-ups made it easy to join without arriving with a group.");
  }
  if (signals.quiz) {
    details.push("The sign-up also included quiz participation.");
  }
  if (signals.workshop) {
    details.push("Workshop choices were part of registration.");
  }
  if (signals.borrel) {
    details.push("The event also included a borrel option for relaxed conversation after the main activity.");
  }

  return [core, ...details].join(" ");
}

function baseEventSummary(event) {
  const normalized = cleanText(event.title).toLowerCase();
  const place = event.regionName;
  if (/dinner|food|bites|trifon|wine|zarezan|bbq|brunch/.test(normalized)) {
    return `A BGSNL dinner in ${place} where Bulgarian students and friends could meet over familiar food and conversation.`;
  }
  if (/game|belot|trivia|quiz|board|beer pong|million|milioner|parichko/.test(normalized)) {
    return `A relaxed BGSNL games event in ${place} for people to meet, play, and join the local Bulgarian community.`;
  }
  if (/party|gala|karaoke|chalgari|dance|y2k|2000s|galentines|pals/.test(normalized)) {
    return `A social BGSNL event in ${place} built around music, conversation, and meeting the Bulgarian student community.`;
  }
  if (/movie|film|cinema/.test(normalized)) {
    return `A BGSNL movie night in ${place} where people could watch together and meet others from the local community.`;
  }
  if (/workshop|career|network|hr|entrepreneur|intern|pwc/.test(normalized)) {
    return `A practical BGSNL event in ${place} connecting Bulgarian students and young professionals through learning and networking.`;
  }
  if (/football|run|sport|workout|tournament|volley|suples/.test(normalized)) {
    return `An active BGSNL event in ${place} for people to move, compete, and meet others from the community.`;
  }
  if (/trip|hunt|picnic|route|маршрутка/.test(normalized)) {
    return `A BGSNL outing in ${place} designed to help people explore, talk, and feel part of the Bulgarian community in the Netherlands.`;
  }
  return `A BGSNL past event in ${place} where Bulgarian students, young professionals, and friends could meet the local community.`;
}

function formatList(values) {
  if (values.length <= 1) return values[0] || "";
  if (values.length === 2) return `${values[0]} and ${values[1]}`;
  return `${values.slice(0, -1).join(", ")}, and ${values.at(-1)}`;
}

function sortArchiveEvents(a, b) {
  if (a.date && b.date) return b.date.localeCompare(a.date) || a.title.localeCompare(b.title);
  if (a.date) return -1;
  if (b.date) return 1;
  return a.regionName.localeCompare(b.regionName) || a.title.localeCompare(b.title) || a.id.localeCompare(b.id);
}

function detectDelimiter(inputPath, source) {
  if (path.extname(inputPath).toLowerCase() === ".tsv") return "\t";
  const firstLine = source.split(/\r?\n/, 1)[0] || "";
  return firstLine.includes("\t") ? "\t" : ",";
}

function parseDelimited(source, delimiter) {
  const rows = [];
  let row = [];
  let field = "";
  let quoted = false;

  for (let index = 0; index < source.length; index += 1) {
    const char = source[index];
    const next = source[index + 1];

    if (char === "\"") {
      if (quoted && next === "\"") {
        field += "\"";
        index += 1;
      } else {
        quoted = !quoted;
      }
      continue;
    }

    if (!quoted && char === delimiter) {
      row.push(field);
      field = "";
      continue;
    }

    if (!quoted && (char === "\n" || char === "\r")) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(field);
      if (row.some((value) => value.trim() !== "")) rows.push(row);
      row = [];
      field = "";
      continue;
    }

    field += char;
  }

  row.push(field);
  if (row.some((value) => value.trim() !== "")) rows.push(row);
  return rows;
}

function rowObject(headers, values) {
  return headers.reduce((row, header, index) => {
    if (!header) return row;
    if (row[header] === undefined) {
      row[header] = values[index] ?? "";
    } else if (Array.isArray(row[header])) {
      row[header].push(values[index] ?? "");
    } else {
      row[header] = [row[header], values[index] ?? ""];
    }
    return row;
  }, {});
}

function firstValue(row, keys) {
  for (const key of keys) {
    const value = row[key];
    if (Array.isArray(value)) {
      const first = value.find((item) => cleanText(item));
      if (first) return first;
    } else if (cleanText(value)) {
      return value;
    }
  }
  return "";
}

function normalizeRegion(value) {
  const normalized = cleanText(value)
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/\+/g, "and")
    .replace(/the hague/g, "hague")
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");

  const aliases = {
    breda: "breda_tilburg",
    tilburg: "breda_tilburg",
    breda_and_tilburg: "breda_tilburg",
    breda_tilburg: "breda_tilburg",
    leiden: "leiden_hague",
    leiden_and_hague: "leiden_hague",
    leiden_hague: "leiden_hague",
    the_hague: "leiden_hague",
    hague: "leiden_hague",
    den_haag: "leiden_hague",
  };

  return aliases[normalized] || normalized;
}

function toIsoDate(value) {
  const raw = cleanText(value);
  if (!raw) return "";

  const serial = Number(raw);
  if (Number.isFinite(serial) && serial > 20000 && serial < 80000) {
    const date = new Date(Date.UTC(1899, 11, 30 + Math.floor(serial)));
    return formatDate(date);
  }

  const iso = raw.match(/^(\d{4})[-/.](\d{1,2})[-/.](\d{1,2})/);
  if (iso) return formatParts(iso[1], iso[2], iso[3]);

  const slash = raw.match(/^(\d{1,2})\/(\d{1,2})\/(\d{2,4})/);
  if (slash) {
    const year = slash[3].length === 2 ? `20${slash[3]}` : slash[3];
    const first = Number(slash[1]);
    const second = Number(slash[2]);
    if (first > 12) return formatParts(year, second, first);
    return formatParts(year, first, second);
  }

  const eu = raw.match(/^(\d{1,2})[-.](\d{1,2})[-.](\d{2,4})/);
  if (eu) {
    const year = eu[3].length === 2 ? `20${eu[3]}` : eu[3];
    return formatParts(year, eu[2], eu[1]);
  }

  const namedMonth = raw.match(/^(\d{1,2})(?:st|nd|rd|th)?\s+([a-z]+)\s+(\d{2,4})/i);
  if (namedMonth) {
    const month = monthNumber(namedMonth[2]);
    if (month) {
      const year = namedMonth[3].length === 2 ? `20${namedMonth[3]}` : namedMonth[3];
      return formatParts(year, month, namedMonth[1]);
    }
  }

  const parsed = new Date(raw);
  return Number.isNaN(parsed.getTime()) ? "" : formatDate(parsed);
}

function monthNumber(value) {
  const months = {
    jan: 1,
    january: 1,
    feb: 2,
    february: 2,
    mar: 3,
    march: 3,
    apr: 4,
    april: 4,
    may: 5,
    jun: 6,
    june: 6,
    jul: 7,
    july: 7,
    aug: 8,
    august: 8,
    sep: 9,
    sept: 9,
    september: 9,
    oct: 10,
    october: 10,
    nov: 11,
    november: 11,
    dec: 12,
    december: 12,
  };
  return months[cleanText(value).toLowerCase()] || "";
}

function formatParts(year, month, day) {
  const yearNumber = Number(year);
  const monthNumberValue = Number(month);
  const dayNumber = Number(day);
  const date = new Date(Date.UTC(yearNumber, monthNumberValue - 1, dayNumber));
  if (Number.isNaN(date.getTime())) return "";
  if (
    date.getUTCFullYear() !== yearNumber
    || date.getUTCMonth() !== monthNumberValue - 1
    || date.getUTCDate() !== dayNumber
  ) {
    return "";
  }
  return formatDate(date);
}

function formatDate(date) {
  return date.toISOString().slice(0, 10);
}

function startOfUtcDay(date) {
  return new Date(Date.UTC(date.getUTCFullYear(), date.getUTCMonth(), date.getUTCDate())).getTime();
}

function inferTags(text) {
  const normalized = cleanText(text).toLowerCase();
  const checks = [
    ["culture", /bulgarian|culture|gala|dinner|horo|folk|tradition|easter|christmas|holiday|ambassador/],
    ["career", /career|pwc|intern|workshop|seminar|entrepreneur|network|alumni/],
    ["social", /party|drink|dinner|game|belot|board|trivia|quiz|karaoke|meet|social|brunch|picnic/],
    ["sports", /sport|tournament|volley|football|run|ski/],
    ["travel", /trip|travel|field|city|visit/],
  ];

  return checks
    .filter(([, re]) => re.test(normalized))
    .map(([tag]) => tag)
    .slice(0, 3);
}

function cleanText(value) {
  return String(value ?? "")
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, "\"")
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

function cleanArchiveEventTitle(value) {
  const title = cleanText(value).replace(/\s*-[a-z0-9]{5}$/i, "");
  return title || cleanText(value);
}

function slugify(value) {
  return cleanText(value)
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80);
}

function titleCase(value) {
  return value.replace(/\b\w/g, (letter) => letter.toUpperCase());
}

