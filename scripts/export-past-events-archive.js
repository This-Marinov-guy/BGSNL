const fs = require("node:fs");
const path = require("node:path");
const { createRequire } = require("node:module");

const DEFAULT_API_DIR = path.resolve(__dirname, "..", "..", "BGSNL-API");
const DEFAULT_OUT = path.resolve(__dirname, "..", "src", "util", "defines", "PAST_EVENTS_ARCHIVE.js");

async function main() {
  const apiDir = path.resolve(process.env.BGSNL_API_DIR || DEFAULT_API_DIR);
  const outPath = path.resolve(process.env.BGSNL_PAST_EVENTS_OUT || DEFAULT_OUT);
  const limit = positiveInteger(process.env.BGSNL_PAST_EVENTS_LIMIT, 80);
  const uri = process.env.BGSNL_MONGODB_URI || readMongoUriFromApiConfig(apiDir);

  if (!uri) {
    throw new Error("Missing BGSNL_MONGODB_URI and no readable BGSNL-API Mongo config was found.");
  }

  const apiRequire = createRequire(path.join(apiDir, "package.json"));
  const { MongoClient } = apiRequire("mongodb");
  const client = new MongoClient(uri);

  try {
    await client.connect();
    const events = client.db().collection("events");
    const rows = await events
      .find(
        {
          hidden: { $ne: true },
          date: { $lt: new Date() },
        },
        {
          projection: {
            _id: 1,
            title: 1,
            newTitle: 1,
            region: 1,
            date: 1,
            correctedDate: 1,
            location: 1,
            description: 1,
            text: 1,
            poster: 1,
            images: 1,
            folder: 1,
            createdAt: 1,
          },
        },
      )
      .sort({ date: -1 })
      .limit(limit)
      .toArray();

    const archive = rows.map(toArchiveEvent).filter(Boolean);
    writeArchive(outPath, archive);
    console.log(`Exported ${archive.length} sanitized past event summaries to ${outPath}`);
  } finally {
    await client.close();
  }
}

function readMongoUriFromApiConfig(apiDir) {
  const configPath = path.join(apiDir, "util", "config", "db.js");
  if (!fs.existsSync(configPath)) return "";
  const source = fs.readFileSync(configPath, "utf8");
  const match = source.match(/const uri = `([^`]+)`/);
  return match?.[1] || "";
}

function toArchiveEvent(event) {
  const id = String(event._id || "").trim();
  const region = cleanText(event.region);
  const title = cleanText(event.newTitle || event.title);
  const date = toIsoDate(event.correctedDate || event.date);

  if (!id || !region || !title || !date) return null;

  const description = excerpt(event.description || event.text, 220);
  const image = publicImage(event.poster || firstImage(event.images));

  return {
    id,
    title,
    region,
    date,
    year: new Date(date).getUTCFullYear(),
    location: cleanText(event.location),
    description,
    image,
    url: `/${region}/event-details/${id}`,
    tags: inferTags(`${title} ${description}`),
  };
}

function writeArchive(outPath, archive) {
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  const body = [
    "// Generated by scripts/export-past-events-archive.js.",
    "// Public event summaries only; attendee and payment data are intentionally excluded.",
    `export const PAST_EVENTS_ARCHIVE_GENERATED_AT = ${JSON.stringify(new Date().toISOString())};`,
    `export const PAST_EVENTS_ARCHIVE = ${JSON.stringify(archive, null, 2)};`,
    "",
  ].join("\n");
  fs.writeFileSync(outPath, body, "utf8");
}

function firstImage(images) {
  return Array.isArray(images) ? images.find(Boolean) : "";
}

function publicImage(value) {
  const image = cleanText(value);
  if (!image) return "";
  if (/^https?:\/\//i.test(image)) return image;
  if (image.startsWith("/")) return image;
  return `/${image.replace(/^\/+/, "")}`;
}

function excerpt(value, maxLength) {
  const text = cleanText(value);
  if (text.length <= maxLength) return text;
  return `${text.slice(0, maxLength - 1).replace(/\s+\S*$/, "")}.`;
}

function cleanText(value) {
  return String(value ?? "")
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, "\"")
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

function toIsoDate(value) {
  const date = new Date(value);
  return Number.isNaN(date.getTime()) ? "" : date.toISOString();
}

function inferTags(text) {
  const normalized = text.toLowerCase();
  const tags = [];
  const checks = [
    ["gala", /gala|palette|may 24/],
    ["career", /career|pwc|intern|workshop|seminar|network/],
    ["culture", /culture|bulgarian|horo|folk|tradition|holiday|easter|christmas/],
    ["social", /party|drink|dinner|game|meet|social/],
    ["sports", /sport|tournament|volley|football/],
  ];

  for (const [tag, re] of checks) {
    if (re.test(normalized)) tags.push(tag);
  }

  return tags.slice(0, 3);
}

function positiveInteger(value, fallback) {
  const parsed = Number(value);
  return Number.isInteger(parsed) && parsed > 0 ? parsed : fallback;
}

if (require.main === module) {
  main().catch((error) => {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  });
}
